interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
}

export function EmojiPicker({ onSelect }: EmojiPickerProps) {
  const [activeCat, setActiveCat] = useState(0);
  const category = CATEGORIES[activeCat]!;

  return (
    <div
      className="flex flex-col overflow-hidden rounded-2xl border border-[var(--border)] shadow-2xl"
      style={{ width: 280, background: "var(--surface-raised)" }}
    >
      {/* Category tabs */}
      <div
        className="flex gap-0.5 border-b border-[var(--border)] px-2 py-1.5 overflow-x-auto"
        style={{ scrollbarWidth: "none" }}
      >
        {CATEGORIES.map((cat, i) => (
          <button
            key={cat.label}
            onClick={() => setActiveCat(i)}
            title={cat.label}
            className={`flex-shrink-0 rounded-lg p-1 text-base leading-none transition-colors ${
              activeCat === i
                ? "bg-[var(--accent)]/20"
                : "hover:bg-[var(--surface-overlay)]"
            }`}
          >
            {cat.icon}
          </button>
        ))}
      </div>

      {/* Emoji grid */}
      <div className="overflow-y-auto p-2" style={{ height: 220, scrollbarWidth: "thin" }}>
        <p className="mb-1 px-1 text-[11px] font-semibold uppercase tracking-wide text-[var(--text-muted)]">
          {category.label}
        </p>
        <div className="grid grid-cols-8 gap-0.5">
          {category.emojis.map((emoji) => (
            <button
              key={emoji}
              onClick={() => onSelect(emoji)}
              className="flex items-center justify-center rounded-lg p-1 text-lg leading-none hover:bg-[var(--surface-overlay)] transition-colors"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}
